#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *r;

	r = (char *)s;
	while (*r != '\0')
	{
		if (*r == c)
			return (r);
		r++;
	}
	if (c == '\0')
		return (r);
	return (NULL);
}
