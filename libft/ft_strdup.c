#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new;
	int		i;

	i = ft_strlen(s);
	new = (char*)malloc(sizeof(*new) * i + 1);
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
